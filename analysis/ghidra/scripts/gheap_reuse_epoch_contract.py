	audit(0x00C3DBF0, "IOManager main-thread completed-task processing", 30000)
	audit(0x008774A0, "cell transition synchronization owner", 40000)
	audit(0x006649D0, "SpeedTree update reader after deferred destruction", 30000)
	audit(0x00CFCC2C, "NiRefObject chain reader from BSTreeNode crash", 30000)
	audit(0x0044DD60, "IO task final release", 20000)
	audit(0x00AA4060, "GameHeap free owner", 24000)
	find_refs_to(0x00AA4060, "GameHeap free owner")
	disasm_range(0x0086E980, 0x0086EE40, "main-loop PDD, AI dispatch, render, maintenance, and AI join ordering")
	disasm_range(0x008774A0, 0x008776E0, "cell transition worker drains and deferred destruction")
